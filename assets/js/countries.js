  /**
   * Generate select options HTML for country dropdowns
   * @param {string} selectedCode - Currently selected country code
   * @param {boolean} africanFirst - Whether to prioritize African countries
   * @returns {string} HTML string with option elements
   */
  generateOptions: function(selectedCode = '', africanFirst = true) {
    let html = '<option value="" data-i18n="forms.selectCountry">Select Country</option>';
    
    if (africanFirst) {
      // Add African countries first
      html += '<optgroup label="African Countries" data-i18n-label="regions.africa">';
      this.africanCountries.forEach(country => {
        const selected = country.code === selectedCode ? ' selected' : '';
        html += `<option value="${country.code}"${selected}>${country.name}</option>`;
      });
      html += '</optgroup>';
      
      // Add other countries
      const otherCountries = this.countries.filter(country => 
        !this.africanCountries.find(african => african.code === country.code)
      );
      html += '<optgroup label="Other Countries" data-i18n-label="regions.other">';
      otherCountries.forEach(country => {
        const selected = country.code === selectedCode ? ' selected' : '';
        html += `<option value="${country.code}"${selected}>${country.name}</option>`;
      });
      html += '</optgroup>';
    } else {
      // All countries alphabetically
      this.countries.forEach(country => {
        const selected = country.code === selectedCode ? ' selected' : '';
        html += `<option value="${country.code}"${selected}>${country.name}</option>`;
      });
    }
    
    return html;
  },

  /**
   * Get country name by code
   * @param {string} code - ISO country code
   * @returns {string} Country name or empty string if not found
   */
  getCountryName: function(code) {
    const country = this.countries.find(c => c.code === code);
    return country ? country.name : '';
  },

  /**
   * Update all country selectors on the page
   * @param {boolean} africanFirst - Whether to prioritize African countries
   */
  updateAllSelectors: function(africanFirst = true) {
    const selectors = document.querySelectorAll('select[data-country-selector]');
    selectors.forEach(select => {
      const currentValue = select.value;
      select.innerHTML = this.generateOptions(currentValue, africanFirst);
      
      // Trigger change event if translation system is available
      if (window.FlowTranslations) {
        window.FlowTranslations.updateTranslations();
      }
    });
  }
